// check character is vowel or consonent

class Vowel
{
  public static void main(String [] args)
  {
    char x='A';
    
    if(x=='a'||x=='e'||x=='i'||x=='o'||x=='u')
    {
      System.out.println("Vowel");
    }
    else if(x=='A' || x=='E' || x=='I' || x=='O' || x=='U')
    {
      System.out.println("vowel");
    }
    else
    System.out.println("consonent");
   }
}
