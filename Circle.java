// Write a program to find the area and circumference of a circle.

class Circle
{
  public static void main(String [] args)
  {
    int r=5;
    double area, circum;
    area=3.14*r*r;
    circum=2*3.14*r;
    System.out.println("Area of circle:" +area);
    System.out.println("Circumference of circle:" +circum);
  }
}
