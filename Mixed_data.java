// Program to display various types of arithmetic operation using mixed datatype

class Mixed_data
{
   public static void main(String [] args)
   {
      int a=5, b=7;
      double c=3.7, d=8.0;
       System.out.println("a+b=" +(a+b));
       System.out.println("c+d=" +(c+d));
       System.out.println("a+d=" +(a+d));
	System.out.println("a-b=" +(a-b));
	System.out.println("c-d=" +(c-d));
	System.out.println("a-d=" +(a-d));
	System.out.println("a*b=" +(a*b));
	System.out.println("c*d=" +(c*d));
	System.out.println("a*d=" +(a*d));
	System.out.println("a/b=" +(a/b));
	System.out.println("c/d=" +(c/d));
	System.out.println("a/d=" +(a/d));
       
   }
}
    